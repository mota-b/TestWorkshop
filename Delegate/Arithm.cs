using System;
using System.Collections.Generic;
using System.Text;

class Arithm
{
    public Arithm()
    {

    }
        
    // Add methode
    public int add(int a, int b)
    {
        return a + b;
    }

    public int add3(int a, int b, int c)
    {
        return a + b+c;
    }

    // Sub methode
    public int sub(int a, int b)
    {
        return a - b;
    }

}
